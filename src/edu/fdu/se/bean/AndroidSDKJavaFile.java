package edu.fdu.se.bean;

public class AndroidSDKJavaFile {
	private int id;
	private String fileName;
	private String fileFullPath;
	private int sdkVersion;
	private String subCategory;
	private String subsubCategory;
	private String subsubCategoryPath;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileFullPath() {
		return fileFullPath;
	}
	public void setFileFullPath(String fileFullPath) {
		this.fileFullPath = fileFullPath;
	}
	public int getSdkVersion() {
		return sdkVersion;
	}
	public void setSdkVersion(int sdkVersion) {
		this.sdkVersion = sdkVersion;
	}
	public String getSubCategory() {
		return subCategory;
	}
	public void setSubCategory(String subCategory) {
		this.subCategory = subCategory;
	}
	public String getSubsubCategory() {
		return subsubCategory;
	}
	public void setSubsubCategory(String subsubCategory) {
		this.subsubCategory = subsubCategory;
	}
	public String getSubsubCategoryPath() {
		return subsubCategoryPath;
	}
	public void setSubsubCategoryPath(String subsubCategoryPath) {
		this.subsubCategoryPath = subsubCategoryPath;
	}
	
	
	

}
